    private void ConsultaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConsultaTableMouseClicked
        int index = ConsultaTable.getSelectedRow();
        TableModel model = ConsultaTable.getModel();
        String anoFabricacao = model.getValueAt(index, 5).toString();
        String modelo = model.getValueAt(index, 2).toString();
        String marca = model.getValueAt(index, 1).toString();
        String placa = model.getValueAt(index, 3).toString();
        String cor = model.getValueAt(index, 4).toString();
        String ID = model.getValueAt(index, 0).toString();
        
        FRM_EditarRegistro formAlteracao =  new FRM_EditarRegistro();
        formAlteracao.TXT_ano.setText(anoFabricacao);
        formAlteracao.TXT_modelo.setText(modelo);
        formAlteracao.TXT_marca.setText(marca);
        formAlteracao.TXT_placa.setText(placa);
        formAlteracao.TXT_cor.setText(cor);
        formAlteracao.lbl.setText(ID);
        formAlteracao.setVisible(true);
    }//GEN-LAST:event_ConsultaTableMouseClicked

