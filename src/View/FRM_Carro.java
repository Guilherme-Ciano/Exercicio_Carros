    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    int anoFabricacao = Integer.parseInt(this.inputAno.getText());
        String modelo = this.inputModelo.getText();
        String marca = this.inputMarca.getText();
        String placa = this.inputPlaca.getText();
        String cor = this.inputCor.getText();
        carroController carCNTR = new carroController();
        try {
            carCNTR.inserir(marca, modelo, placa, cor, anoFabricacao);
        } catch (SQLException ex) {
            Logger.getLogger(FRM_Carro.class.getName()).log(Level.SEVERE, null, ex);
            this.status.setText("Placa já existente, tente outra!");
            this.status.setText("Erro!");
        }
        this.status.setText("Dados gravados com sucesso!");
    }//GEN-LAST:event_jButton1MouseClicked

