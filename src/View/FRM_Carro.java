    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        DefaultTableModel dados = (DefaultTableModel) ConsultaTable.getModel();
        dados.setNumRows(0);
        carroController carCNTR = new carroController();
        ArrayList carros = new ArrayList();
        carros = carCNTR.buscarCarros();
        Iterator it = carros.iterator();
        while (it.hasNext()){
            carroModel car = (carroModel) it.next();
            dados.addRow(new Object[]{car.getIdCarros(), car.getMarca(), car.getModelo(),
                car.getPlaca(), car.getCor(), car.getAnoFabricacao()});
        }
    }//GEN-LAST:event_jButton3MouseClicked

